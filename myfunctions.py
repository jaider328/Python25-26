import turtle
bob=turtle.Turtle()

#triangle
def triangle():
    for times in range(3):
        bob.forward(100)
        bob.left(120)

#square
def square():
    for times in range(4):
           bob.forward(100)
           bob.left(100)
        
#pentagon
def pentagon():
    for times in range(5):
        bob.forward(100)
        bob.left(72)
        
    
